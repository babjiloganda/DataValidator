package com.data.validator.rule;

public class Rule {
    private String name;
    private String rawRule;

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getRawRule() {
        return rawRule;
    }

    public void setRawRule(final String rawRule) {
        this.rawRule = rawRule;
    }
}
